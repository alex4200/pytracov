
def add_numbers(a, b):
    """Returns the addition of the two numbers a and b.
    """
    return a + b

def multiply_numbers(a, b):
    """Returns the multiplication of the two numbers a and b.
    """
    return a * b

def diff_numbers(a, b):
    """Returns the absolute difference of the two numbers a and b.
    """
    return abs(a - b)